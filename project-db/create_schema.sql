CREATE SCHEMA PROJECT;

CREATE TABLE PROJECT.MESSAGES (
		MESSAGE_ID INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY ( START WITH 1 INCREMENT BY 1 MINVALUE 1 MAXVALUE 2147483647 NO CYCLE CACHE 20),
		MESSAGE_CREATED TIMESTAMP NOT NULL DEFAULT CURRENT TIMESTAMP,
		MESSAGE_MODIFIED TIMESTAMP NOT NULL GENERATED BY DEFAULT FOR EACH ROW ON UPDATE AS ROW CHANGE TIMESTAMP,
		MESSAGE_TEXT VARCHAR(512) NOT NULL
)DATA CAPTURE NONE;

CREATE UNIQUE INDEX PROJECT.MESSAGES_PK_IDX
	ON PROJECT.MESSAGES
	(MESSAGE_ID		ASC) PCTFREE 0
ALLOW REVERSE SCANS;

ALTER TABLE PROJECT.MESSAGES ADD CONSTRAINT MESSAGES_PK PRIMARY KEY
	(MESSAGE_ID);


